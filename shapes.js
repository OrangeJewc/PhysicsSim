function Square(x, y, width) {
	this.x = x;
	this.y = y;
	this.width = width;
}

function Circle(x, y, radius) {
	this.x = x;
	this.y = y;
	this.radius = radius;
}

function Triangle(x, y, side) {
	this.x = x;
	this.y = y;
	this.side = side;
}

function Line(x1, x2, y1, y2) {
	this.x1 = x1;
	this.x2 = x2;
	this.y1 = y1;
	this.y2 = y2;
}

